<template>
  <div class="app-container">
    <!--工具栏-->
    <div class="head-container">
      <div v-if="crud.props.searchToggle">
        <!-- 搜索 -->
        <el-input v-model="crud.query.location" clearable placeholder="地理位置" style="width: 150px;" class="filter-item" @keyup.enter.native="crud.toQuery" />
        <el-select v-model="crud.query.isDomestic" clearable placeholder="是否国内" style="width: 120px;" class="filter-item">
          <el-option label="国内" :value="true" />
          <el-option label="国外" :value="false" />
        </el-select>
        <el-input-number v-model="crud.query.ipCount" placeholder="IP数量" style="width: 150px;" class="filter-item" :min="0" />
        <el-date-picker
          v-model="crud.query.statTime"
          type="datetime"
          placeholder="选择统计时间"
          value-format="yyyy-MM-ddTHH:mm:ss"
          style="width: 200px;"
          class="filter-item"
        />
        <el-button class="filter-item" size="mini" type="primary" icon="el-icon-search" @click="crud.toQuery">搜索</el-button>
        <el-button class="filter-item" size="mini" type="warning" icon="el-icon-refresh-left" @click="crud.resetQuery()">重置</el-button>
      </div>
      <crudOperation :permission="permission" :crud="crud" />
    </div>
    
    <!--表单组件-->
    <el-dialog :close-on-click-modal="false" :before-close="crud.cancelCU" :visible.sync="crud.status.cu > 0" :title="crud.status.title" width="500px">
      <el-form ref="form" :model="form" :rules="rules" size="small" label-width="100px">
        <el-form-item label="地理位置" prop="location">
          <el-input v-model="form.location" style="width: 370px;" />
        </el-form-item>
        <el-form-item label="是否国内" prop="isDomestic">
          <el-select v-model="form.isDomestic" style="width: 370px;">
            <el-option label="国内" :value="true" />
            <el-option label="国外" :value="false" />
          </el-select>
        </el-form-item>
        <el-form-item label="IP数量" prop="ipCount">
          <el-input-number v-model="form.ipCount" :min="0" style="width: 370px;" />
        </el-form-item>
        <el-form-item label="统计时间" prop="statTime">
          <el-date-picker
            v-model="form.statTime"
            type="datetime"
            style="width: 370px;"
            value-format="yyyy-MM-ddTHH:mm:ss"
            placeholder="选择日期时间"
          />
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button type="text" @click="crud.cancelCU">取消</el-button>
        <el-button :loading="crud.status.cu === 2" type="primary" @click="crud.submitCU">确认</el-button>
      </div>
    </el-dialog>
    <!--表格渲染-->
    <el-table ref="table" v-loading="crud.loading" :data="crud.data" highlight-current-row stripe style="width: 100%" @selection-change="crud.selectionChangeHandler">
      <el-table-column type="selection" width="55" />
      <el-table-column prop="id" label="ID" width="80px" />
      <el-table-column prop="location" label="地理位置" />
      <el-table-column prop="isDomestic" label="是否国内" width="100">
        <template slot-scope="scope">
          <el-tag :type="getIsDomesticType(scope.row.isDomestic)">{{ getIsDomesticText(scope.row.isDomestic) }}</el-tag>
        </template>
      </el-table-column>
      <el-table-column prop="ipCount" label="IP数量" width="100">
        <template slot-scope="scope">
          <el-tag type="primary">{{ scope.row.ipCount }}</el-tag>
        </template>
      </el-table-column>
      <el-table-column prop="statTime" label="统计时间" width="180">
        <template slot-scope="scope">
          {{ parseTime(scope.row.statTime) }}
        </template>
      </el-table-column>
      <el-table-column prop="createdAt" label="创建时间" width="180">
        <template slot-scope="scope">
          {{ parseTime(scope.row.createdAt) }}
        </template>
      </el-table-column>
      <el-table-column prop="updatedAt" label="更新时间" width="180">
        <template slot-scope="scope">
          {{ parseTime(scope.row.updatedAt) }}
        </template>
      </el-table-column>
      <el-table-column v-if="checkPer(['admin','outboundIp:edit','outboundIp:del'])" label="操作" width="150px" align="center">
        <template slot-scope="scope">
          <udOperation
            :data="scope.row"
            :permission="permission"
            :crud="crud"
          />
        </template>
      </el-table-column>
    </el-table>
    <!--分页组件-->
    <pagination :crud="crud" />
  </div>
</template>

<script>
import crudOutboundIp from '@/api/stat/outboundIp'
import CRUD, { presenter, header, form, crud } from '@crud/crud'
import rrOperation from '@crud/RR.operation'
import crudOperation from '@crud/CRUD.operation'
import udOperation from '@crud/UD.operation'
import pagination from '@crud/Pagination'
import { parseTime } from '@/utils/index'

const defaultForm = { 
  id: null,
  location: null,
  isDomestic: true,
  ipCount: 0,
  statTime: new Date().toISOString().split('.')[0]
}

export default {
  name: 'OutboundIpStat',
  components: { pagination, crudOperation, rrOperation, udOperation },
  cruds() {
    return CRUD({ 
      title: '外联IP统计',
      url: 'api/stat/outbound-ip',
      sort: ['statTime,desc'],
      crudMethod: { ...crudOutboundIp },
      optShow: {
        add: true,
        edit: true,
        del: true,
        download: false
      }
    })
  },
  mixins: [presenter(), header(), form(defaultForm), crud()],
  data() {
    return {
      permission: {
        add: ['admin', 'outboundIp:add'],
        edit: ['admin', 'outboundIp:edit'],
        del: ['admin', 'outboundIp:del']
      },
      rules: {
        location: [
          { required: true, message: '请输入地理位置', trigger: 'blur' }
        ],
        isDomestic: [
          { required: true, message: '请选择是否国内', trigger: 'change' }
        ],
        ipCount: [
          { required: true, message: '请输入IP数量', trigger: 'blur' },
          { type: 'number', message: '数量必须为数字', trigger: 'blur' }
        ],
        statTime: [
          { required: true, message: '请选择统计时间', trigger: 'change' }
        ]
      }
    }
  },
  created() {
    // 初始化查询参数
    this.crud.query = {
      location: null,
      isDomestic: null,
      ipCount: null,
      statTime: null
    }
  },
  methods: {
    // 获取是否国内对应的标签类型
    getIsDomesticType(isDomestic) {
      return isDomestic ? 'success' : 'warning';
    },
    // 获取是否国内显示文本
    getIsDomesticText(isDomestic) {
      return isDomestic ? '国内' : '国外';
    },
    // 权限检查
    checkPer(permissions) {
      if (permissions && permissions instanceof Array && permissions.length > 0) {
        const roles = this.$store.getters && this.$store.getters.roles
        const permissionRoles = permissions
        return roles.some(role => {
          return permissionRoles.includes(role)
        })
      } else {
        return false
      }
    },
    // 格式化时间
    parseTime
  }
}
</script>

<style scoped>
.date-item {
  display: inline-block;
  margin-right: 8px;
}
